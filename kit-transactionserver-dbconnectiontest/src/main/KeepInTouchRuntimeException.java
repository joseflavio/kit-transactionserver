package main;


public abstract class KeepInTouchRuntimeException extends RuntimeException {

  private static final long serialVersionUID = -8183548114420512022L;

  public KeepInTouchRuntimeException() {
    super();
  }

  public KeepInTouchRuntimeException(Throwable cause) {
    super(cause);
  }

  public KeepInTouchRuntimeException(String message, Throwable cause) {
    super(message, cause);
  }

}// exception class
